import json
import os
import re
import pandas as pd
from PIL import Image

import torch
from torch.utils.data import Dataset
import torchvision.transforms as transforms


def _clean_report(text: str, max_words: int = 60) -> str:
    """Normalise raw IU X-Ray report text and cap at max_words words."""
    text = text.lower().strip()
    text = re.sub(r'\bxxxx\b', '', text)
    text = re.sub(r'\s+', ' ', text)
    text = re.sub(r'[^\w\s.,;:\-]', '', text)
    text = text.strip()
    words = text.split()
    if len(words) > max_words:
        text = " ".join(words[:max_words])
    return text


class IUXrayMultiViewDataset(Dataset):

    def __init__(self, root_dir, split="train"):

        self.root_dir = root_dir
        self.image_dir = os.path.join(root_dir, "images", "images_normalized")

        reports_path = os.path.join(root_dir, "indiana_reports.csv")
        projections_path = os.path.join(root_dir, "indiana_projections.csv")
        _repo_root = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
        split_path = os.path.join(_repo_root, "data", "splits", "iu_split.csv")

        # ── Load pre-extracted Stanza entity tags (optional) ─────────────
        # Generated by: python scripts/prepare/extract_entities.py
        entity_tags_path = os.path.join(_repo_root, "store", "entity_tags.json")
        self._entity_tags: dict = {}
        if os.path.exists(entity_tags_path):
            with open(entity_tags_path, "r", encoding="utf-8") as f:
                self._entity_tags = json.load(f)

        # ------------------------
        # Load CSV files
        # ------------------------

        reports = pd.read_csv(reports_path)
        projections = pd.read_csv(projections_path)
        split_df = pd.read_csv(split_path)

        # ------------------------
        # Select split
        # ------------------------

        split_uids = split_df[split_df["split"] == split]["uid"]

        reports = reports[reports["uid"].isin(split_uids)]
        projections = projections[projections["uid"].isin(split_uids)]

        # ------------------------
        # Merge tables
        # ------------------------

        merged = reports.merge(projections, on="uid")

        # PKARG paper (Izhar et al. 2025): use FINDINGS ONLY as the generation
        # target.  The impression section is kept separately as auxiliary
        # knowledge that conditions the generator (not predicted as output).
        # Previously we merged findings+impression → artificially long targets
        # → brevity penalty inflated → BLEU suppressed.
        merged["findings_clean"] = merged["findings"].fillna("").apply(
            lambda x: _clean_report(x, max_words=60)
        )
        # Impression kept raw (cleaned but not capped) for conditioning use
        merged["impression_clean"] = merged["impression"].fillna("").apply(
            lambda x: _clean_report(x, max_words=40)
        )

        # ------------------------
        # Group by patient
        # ------------------------

        grouped = merged.groupby("uid")

        self.samples = []

        for uid, group in grouped:

            image_files  = list(group["filename"])
            findings     = group["findings_clean"].iloc[0]
            impression   = group["impression_clean"].iloc[0]
            entity_tags  = self._entity_tags.get(str(uid), "")

            if len(image_files) >= 1:
                self.samples.append({
                    "uid":        uid,
                    "images":     image_files,
                    "report":     findings,    # findings only — generation target
                    "impression": impression,  # auxiliary knowledge input
                    "entity_tags": entity_tags,  # Stanza entities (may be "")
                })

        print(f"{split} samples:", len(self.samples))

        # ------------------------
        # Image transforms
        # Train uses augmentation; val/test use deterministic resize only.
        #
        # CXR-safe augmentation rules:
        #  ✗ No horizontal flip  — left/right heart position is diagnostic
        #  ✗ No vertical flip    — lung anatomy is orientation-dependent
        #  ✗ No large rotation   — > 15° not realistic for real acquisitions
        #  ✓ RandomResizedCrop   — patient distance / zoom variation
        #  ✓ Small rotation      — patient positioning on the table
        #  ✓ Brightness/contrast — scanner exposure variation
        #  ✓ GaussianBlur        — different scanner sharpness / motion blur
        #  ✓ RandomAdjustSharpness — detail emphasis variation
        #  ✓ RandomEqualize      — global histogram / CLAHE-like normalisation
        #  ✓ RandomErasing       — occlusion robustness (foreign objects, wires)
        #
        # Each view (frontal, lateral) gets the transform applied independently
        # with different random seeds, so the two views in a pair always receive
        # a different random augmentation — giving SimCLR-style contrastive pairs
        # at zero extra cost.
        # ------------------------
        _norm = transforms.Normalize(
            mean=[0.485, 0.456, 0.406],
            std=[0.229, 0.224, 0.225],
        )

        if split == "train":
            self.transform = transforms.Compose([
                # Geometry — simulate patient positioning and zoom
                transforms.RandomResizedCrop(224, scale=(0.75, 1.0), ratio=(0.9, 1.1)),
                transforms.RandomRotation(degrees=15),
                transforms.RandomAffine(degrees=0, translate=(0.05, 0.05)),

                # Intensity — simulate scanner and exposure variation
                transforms.ColorJitter(brightness=0.3, contrast=0.3),
                transforms.RandomAdjustSharpness(sharpness_factor=2, p=0.3),
                transforms.RandomEqualize(p=0.2),  # CLAHE-like global contrast
                transforms.RandomApply(
                    [transforms.GaussianBlur(kernel_size=5, sigma=(0.1, 1.5))],
                    p=0.3,
                ),

                # Convert to tensor before pixel-level augmentations
                transforms.ToTensor(),
                _norm,

                # Occlusion — simulate wires, tubes, foreign objects
                transforms.RandomErasing(
                    p=0.2, scale=(0.02, 0.08), ratio=(0.3, 3.0), value=0
                ),
            ])
        else:
            self.transform = transforms.Compose([
                transforms.Resize((224, 224)),
                transforms.ToTensor(),
                _norm,
            ])

    def __len__(self):
        return len(self.samples)


    def __getitem__(self, idx):

        sample = self.samples[idx]

        images = []

        # ------------------------
        # Load up to 2 views
        # ------------------------

        for img_name in sample["images"][:2]:

            img_path = os.path.join(self.image_dir, img_name)

            image = Image.open(img_path).convert("RGB")
            image = self.transform(image)

            images.append(image)

        # If only one image exists → duplicate it
        if len(images) == 1:
            images.append(images[0])

        images = torch.stack(images)

        # Return 4-tuple: (images, findings_target, impression_aux, entity_tags)
        #  findings_target : str — generation target (findings section only)
        #  impression_aux  : str — auxiliary knowledge for generator conditioning
        #  entity_tags     : str — Stanza NER entities, e.g.
        #                          "problems: effusion; anatomies: lungs"
        #                          empty string "" if extract_entities not run yet
        return images, sample["report"], sample["impression"], sample["entity_tags"]